Ještě neznám váš kód, takže nevím, který část k němu chcete přidat odkazy. Podle vaší popisné fáze, jsem vytvořil následující úpravy v PHP kódu:

```php
<div id="positions-list">
    <a href="#positions-list">Aktivní pozice</a>
    <!-- Zobrazte seznam pozic -->
</div>

<a href="/company_connections.php">Spojení na akci</a>
<a href="/company_meetings.php?status=hired">PLÁCLI JSME SI!</a>
<a href="/company_meetings.php?status=pending">Nové žádosti</a>
```

Tento kód nahradí stávající linkové metriky klikatelnými odkazy. Nezapomeňte nahradit `<!-- Zobrazte seznam pozic -->` kódem pro zobrazení seznamu pozic.

Pokud potřebujete zadat další kroky nebo vyžádané úpravy, můžete odpovědět na tento dotaz.


